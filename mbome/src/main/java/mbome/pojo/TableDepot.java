package mbome.pojo;

public class TableDepot {
	
	private String NumDepot;
	private String NumClient;
	private String CAT;
	private String DateDepot;
	private String DateRetrait;
	private String Archive;
	
	public String getNumDepot() {
		return NumDepot;
	}
	public void setNumDepot(String numDepot) {
		NumDepot = numDepot;
	}
	public String getNumClient() {
		return NumClient;
	}
	public void setNumClient(String numClient) {
		NumClient = numClient;
	}
	public String getCAT() {
		return CAT;
	}
	public void setCAT(String cAT) {
		CAT = cAT;
	}
	public String getDateDepot() {
		return DateDepot;
	}
	public void setDateDepot(String dateDepot) {
		DateDepot = dateDepot;
	}
	public String getDateRetrait() {
		return DateRetrait;
	}
	public void setDateRetrait(String dateRetrait) {
		DateRetrait = dateRetrait;
	}
	public String getArchive() {
		return Archive;
	}
	public void setArchive(String archive) {
		Archive = archive;
	}

}
