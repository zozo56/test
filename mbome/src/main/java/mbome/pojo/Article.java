package mbome.pojo;

public class Article {
	
	private String NumArticle;
	private String NumDepot;
	private String NumClient; 
	private String DescProduit; 
	private String Age; 
	private String PrixVente; 
	private String PrixDepot; 
	private String DateVente; 
	private String Vendu;
	public String getNumArticle() {
		return NumArticle;
	}
	public void setNumArticle(String numArticle) {
		NumArticle = numArticle;
	}
	public String getNumDepot() {
		return NumDepot;
	}
	public void setNumDepot(String numDepot) {
		NumDepot = numDepot;
	}
	public String getNumClient() {
		return NumClient;
	}
	public void setNumClient(String numClient) {
		NumClient = numClient;
	}
	public String getDescProduit() {
		return DescProduit;
	}
	public void setDescProduit(String descProduit) {
		DescProduit = descProduit;
	}
	public String getAge() {
		return Age;
	}
	public void setAge(String age) {
		Age = age;
	}
	public String getPrixVente() {
		return PrixVente;
	}
	public void setPrixVente(String prixVente) {
		PrixVente = prixVente;
	}
	public String getPrixDepot() {
		return PrixDepot;
	}
	public void setPrixDepot(String prixDepot) {
		PrixDepot = prixDepot;
	}
	public String getDateVente() {
		return DateVente;
	}
	public void setDateVente(String dateVente) {
		DateVente = dateVente;
	}
	public String getVendu() {
		return Vendu;
	}
	public void setVendu(String vendu) {
		Vendu = vendu;
	} 

}
